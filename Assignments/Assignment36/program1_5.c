{
    char Arr[50] = {'\0'};
    int iRet = 0;

    printf("Enter String :\n");
    scanf("%[^'\n']s",Arr);

    iRet = CountWhite(Arr);

    printf("Number of White spaces are:%d\n",iRet);

}
