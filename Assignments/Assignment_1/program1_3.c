/////////////////////////////////////////////////////////////////////////////
//  Function name:  DisplayNumbers  
//  Description:    It is used to display 5 to 1 numbers
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void DisplayNumbers()
{
    int i=0;
    i =5;

    while(i>0)
    {
        printf("%d",i);
        i--;
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    DisplayNumbers();

    return 0;
}
