/////////////////////////////////////////////////////////////////////////////
# define PI 3.14

double CircleArea(float fRadius)
{
    double dArea = 0.0;

    dArea= PI * fRadius * fRadius;

    return dArea;

}
//Time complexity:O(1)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter radius");
    scanf("%f",&fValue);

    dRet=CircleArea(fValue);

    printf("Area of circle is %f",dRet);

    return 0;
}
