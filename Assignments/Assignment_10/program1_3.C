#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  KmtoMeter  
//  Description:    It is used to convert distance from kilometer to meter
//  Input:          int
//  Output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////

int KmtoMeter(int iNo)
{
    iNo=iNo*1000;
    
    return iNo;
}
//Time complexity:O(1)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0,iRet=0;

    printf("Enter distance:");
    scanf("%d",&iValue);

    iRet = KmtoMeter(iValue);

    printf("Distance in meter is:%d",iRet);
    return 0;
}
