#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  FhtoCs
//  Description:    It is used to convert tempreture from farhenheit to celcious
//  Input:          double
//  Output:         double
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////

double FhtoCs(double fTemp)
{
    
    fTemp=((fTemp-10)*(5.0/9.0));

    return fTemp;
}
//Time complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    double fValue=0.0;
    double dRet = 0.0;

    printf("Enter Tempreture in fahrenheit");
    scanf("%f",&fValue);


    dRet=FhtoCs(fValue);

    printf("Tempretur in celcious is:%f",dRet);

    return 0;

}
