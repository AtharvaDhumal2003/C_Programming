#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  SquareMeter
//  Description:    It is used convert square feet to square meter
//  Input:          int
//  Output:         double
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iNo)
{
    double dSM=0.0;

    dSM = iNo*0.0929;

    return dSM;
}
//Time complexity:O(1)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    double dRet=0.0;

    printf("Enter area in square feet");
    scanf("%d",&iValue);

    dRet = SquareMeter(iValue);

    printf("Area in squaremeter is%f",dRet);
    return 0;
}
