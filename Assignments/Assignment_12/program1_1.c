/////////////////////////////////////////////////////////////////////////////
//  Function name:  Print_Factors
//  Description:    It is used to display factors of input number
//  Input:          int 
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void print_Factors(int iNo)
{
    int iCnt =0;

    for(iCnt=1;iCnt<=iNo/2;iCnt++)
    {
        if((iNo%iCnt)==0)
        {
            printf("%d",iCnt);
        }
    }
}
//Time Complexity:O(N/2)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNumber;

    printf("Enter Number:");
    scanf("%d",&iNumber);

    print_Factors(iNumber);

    return 0;
}
