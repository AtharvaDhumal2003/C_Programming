#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Count_Factors
//  Description:    It is used to display number of factors of input number
//  Input:          int 
//  output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
/////////////////////////////////////////////////////////////////////////////
int Count_Factors(int iNo)
{
    int iCnt =0;
    int icount=0;
    for(iCnt=1;iCnt<=iNo/2;iCnt++)
    {
        if((iNo%iCnt)==0)
        {
            icount++;
        }
    }

    return icount;
}
//Time Complexity:O(N/2)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////


int main()
{
    int iNumber;

    printf("Enter Number:");
    scanf("%d",&iNumber);

    printf("%d\n",Count_Factors(iNumber));

    return 0;
}
