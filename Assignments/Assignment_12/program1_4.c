#include<stdio.h>
//////////////////////////////////////////////////////////////////////////////
//  Function name:  Sum_Of_Even_Factors
//  Description:    It is used to display sum of even factors of input number
//  Input:          int 
//  output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
//////////////////////////////////////////////////////////////////////////////

int Sum_of_Even_Factors(int iNo)
{
    int iCnt =0;
    int iSum=0;
    for(iCnt=1;iCnt<=iNo/2;iCnt++)
    {
        if((iNo%iCnt)==0 && (iCnt%2)==0)
        {
            iSum=iSum+iCnt;
        }
    }

    return iSum;
}
//Time Complexity:O(N/2)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNumber;

    printf("Enter Number:");
    scanf("%d",&iNumber);

    printf("%d\n",Sum_of_Even_Factors(iNumber));

    return 0;
}
