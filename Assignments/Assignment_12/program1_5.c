#include<stdio.h>
#include<stdbool.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  is_divisible_by_five
//  Description:    It is used to check if input is divisible by 5 or not
//  Input:          int 
//  output:         bool
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
/////////////////////////////////////////////////////////////////////////////
bool is_divisible_by_five(int iNo)
{
    if((iNo%5)==0)
    {
        return true;
    }
    else
    {
        return false;
    }
}
//Time Complexity:O(1)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iNumber=0;

    printf("Enter Number:");
    scanf("%d",&iNumber);

    printf("%s\n", is_divisible_by_five(iNumber) ? "Yes":"No" );
    return 0;
}
