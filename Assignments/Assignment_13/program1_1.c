#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Print_Numbers
//  Description:    It is used to display numbers till  input number
//  Input:          int 
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void print_numbers(int limit)
{
    int iCnt =0;

    for(iCnt=1;iCnt<=limit;iCnt++)
    {
        printf("%d\t",iCnt);
    }
}
//Time Complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////


int main()
{
    int limit;

    printf("Enter Number:");
    
    scanf("%d",&limit);
    print_numbers(limit);

    return 0;
}
