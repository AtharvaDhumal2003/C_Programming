void print_Even_numbers(int limit)
{
    int iCnt =0;

    for(iCnt=1;iCnt<=limit;iCnt++)//+=2 test
    {
        if((iCnt%2)==0)
        {
            printf("%d\t",iCnt);
        }
    }
}
//Time Complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////



int main()
{
    int limit;

    printf("Enter Number:");
    
    scanf("%d",&limit);
    print_Even_numbers(limit);

    return 0;
}
