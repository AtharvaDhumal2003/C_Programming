/////////////////////////////////////////////////////////////////////////////
//  Function name:  sum_natural_Numbers
//  Description:    It is used to display sum of natural numbers  input number
//  Input:          int 
//  output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
/////////////////////////////////////////////////////////////////////////////

int sum_natural_numbers(int limit)
{
    int iCnt =0;
    int iSum =0;

    for(iCnt=1;iCnt<=limit;iCnt++)
    {
        iSum=iSum+iCnt;
    }
    
    printf("%d\t",iSum);

    return 0;

}
//Time Complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////


int main()
{
    int limit;

    printf("Enter Number:");
    
    scanf("%d",&limit);
    

    printf("%d\n",sum_natural_numbers(limit));

    return 0;
}
