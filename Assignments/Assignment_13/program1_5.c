void Sum_Even_numbers(int limit)
{
    int iCnt =0;
    int iSum = 0;

    for(iCnt=1;iCnt<=limit;iCnt++)
    {
        if((iCnt%2)==0)
        {
          iSum=iSum+iCnt;  
        }
    }
    printf("%d",iSum);
}
//Time Complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////



int main()
{
    int limit;

    printf("Enter Number:");
    
    scanf("%d",&limit);
    Sum_Even_numbers(limit);

    return 0;
}
