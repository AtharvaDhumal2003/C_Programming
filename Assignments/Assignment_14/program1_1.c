/////////////////////////////////////////////////////////////////////////////
//  Function name:  DisplayDigits
//  Description:    It is used to display digits of given input in reverse
//  Input:          int 
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
/////////////////////////////////////////////////////////////////////////////

void DisplayDigits(int iNo)
{
    int iDigit = 0;

    if(iNo < 0)
    {
        iNo=-iNo;
    }

    
    while(iNo != 0)
    {
        iDigit=iNo % 10;
        printf("\n%d\t",iDigit);
        iNo=iNo / 10;
    }
}
//Time Complexity:O(Log N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number:");
    scanf("%d",&iValue);

    DisplayDigits(iValue);

    return 0;
}
