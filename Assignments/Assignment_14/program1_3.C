/////////////////////////////////////////////////////////////////////////////
//  Function name:  Print_Odd_Numbers
//  Description:    It is used to display numbers till  input number
//  Input:          int 
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/10/2025
//
