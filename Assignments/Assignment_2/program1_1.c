/////////////////////////////////////////////////////////////////////////////
//  Function name:  AcceptNumberDisplayStar 
//  Description:    It takes input and displays that number of *
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void AcceptNumberDisplayStar(int iNo)
{
    int icnt = 0;

    if(iNo<0)
    {
        iNo=-iNo;
    }
    
    while(icnt <= (iNo-1))
    {
        printf("*\t");
        icnt ++;
    }
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;

    printf("Enter number");
    scanf("%d",&iValue);

    AcceptNumberDisplayStar(iValue);
    return 0;
}
