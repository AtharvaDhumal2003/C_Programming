#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//  Function name:  DisplayHellodemo 
//  Description:    It checks if input is less than 10 or not and displays 
//                  hello or demo acoording to it.
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void DisplayHelloDemo(int iNo)
{
    if(iNo < 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number");
    scanf("%d",&iValue);

    DisplayHelloDemo(iValue);

    return 0;
}
