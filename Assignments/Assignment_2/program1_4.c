void Display(int iNo, int iFrequency)
{
    int icnt = 0;

    if(iFrequency<0)
    {
        iFrequency=-iFrequency;
    }

    for(icnt = 1; icnt <= iFrequency; icnt++)
    {
        printf("%d\t",iNo);
    }
     
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iCount = 0;

    printf("Enter number");
    scanf("%d",&iValue);

    
    printf("Enter frequency");
    scanf("%d",&iCount);

    Display(iValue,iCount);

    return 0;
}
