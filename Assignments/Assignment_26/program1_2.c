/////////////////////////////////////////////////////////////////////////////
//  Function name:  Pattern
//  Description:    It is used to Display
//                  5 # 4 # 3 # 2 # 1 #
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           20/11/2025
//
/////////////////////////////////////////////////////////////////////////////
void Pattern(int iNo)
{
    int iCnt = 0; 
    if(iNo < 0)
    {
        iNo = -iNo;
    }
    for(iCnt =iNo; iCnt >= 1;iCnt--)
    {
        printf("%d\t#\t",iCnt);
    }
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the Frequency:\n");
    scanf("%d",&iValue);

    Pattern(iValue);
    return 0;
}
