#include<stdio.h>
#include<ctype.h>
#include<stdbool.h>

/////////////////////////////////////////////////////////////////////////////
//  Function name:  DislpayConvert  
//  Description:    It is used to take character input if it is uppercase
//                  it converts it to lowercase and viceversa 
//  Input:          char
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////

void DisplayConvert(char CValue)
{
    if(isupper(CValue)){
        printf("%c", tolower(CValue));
    }
    else if(islower(CValue)){
        printf("%c", toupper(CValue));
    }
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter character\n");
    scanf("%c",cValue);

    DisplayConvert(cValue);

    return 0;
}
