#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to convert upper case to lower case and vice versa
//                  and non alphabet displays as it is.
//  Input:          char
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/11/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display(char ch)
{

    if(ch >= 'a' && ch <= 'z')
    {
        ch = ch - 32;
        printf("%c\n",ch);
    }
    else if(ch >= 'A' && ch <= 'Z')
    {
        ch = ch + 32;
        printf("%c\n",ch);
    }
    else
    {
        printf("%c\n",ch);
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter the character :\n");
    scanf("%c",&cValue);

    Display(cValue);
    return 0;
}
