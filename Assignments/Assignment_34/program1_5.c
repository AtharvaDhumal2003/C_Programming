#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display decimal,hexadecimal,octal value of 
//                  given character.
//  Input:          char
//  Author:         Atharva Sanjay Dhumal.
//  Date:           28/11/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display(char ch)
{
    printf("Decimal:%d\n",ch);
    printf("HexaDecimal :%#X\n",ch);
    printf("Octal:%o\n",ch);
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter the character :\n");
    scanf("%c",&cValue);

    Display(cValue);


    return 0;
}
