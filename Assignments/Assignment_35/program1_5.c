void Reverse(char str[])
{
    char *Start = str;
    char *End = str;
    char temp = '\0';

    while(*End != '\0')
    {
        End++;
    }

    End--;
    
    while(Start < End)
    {
        temp = *Start;
        *Start = *End;
        *End = temp;
        Start++;
        End--;
    }

    printf("\n");

}
