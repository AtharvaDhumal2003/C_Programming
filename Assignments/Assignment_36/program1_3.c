/////////////////////////////////////////////////////////////////////////////
//  Function name:  strtoggleX
//  Description:    It use to convert small letters of given string into 
//                  capital letters and viceversa.
//  Input:          char
//  Author:         Atharva Sanjay Dhumal.
//  Date:           03/12/2025
//
/////////////////////////////////////////////////////////////////////////////
void strtoggleX(char str[])
{
    int iCount = 0;
    
    while(*str != '\0')
    {
        if(*str >= 'a' && *str <= 'z')
        {
            *str = *str - 32;
        }
        else if(*str >= 'A' && *str <= 'Z')
        {
            *str = *str + 32;
        }
        str++;
    }

}
