void strCpyX(char *str, char *dest)
{
 
    while(*str != '\0')
    {
        if(*str == ' ')
        {
            *str++;
        }
        *dest = *str;
        str++;
        dest++;
    }
    dest = '\0';

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};
    char Brr[50] = {'\0'};

    printf("Enter String :\n");
    scanf("%[^'\n']s",Arr);

    strCpyX(Arr,Brr);

    printf("%s\n",Brr);

}
