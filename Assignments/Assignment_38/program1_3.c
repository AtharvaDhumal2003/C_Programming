void  StrCpyCap(char *str, char *dest)
{
    char *temp = str;
 
    while(*str != '\0')
    {
        if(*str >= 'a' && *str <= 'z')
        {
            *str = *str - 32;
        }
        *dest = *str;
        str++;
        dest++;
    }
    dest = '\0';

}
