void StrCpySmall(char *str, char *dest)
{
    char *temp = str;
 
    while(*str != '\0')
    {
        if(*str >= 'A' && *str <= 'Z')
        {
            *str = *str + 32;
        }
        *dest = *str;
        str++;
        dest++;
    }
    dest = '\0';

}
