/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display 1 to 5 numbers using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display()
{
    static int iCount = 1;

    if(iCount <= 5)
    {
        printf("%d\t",iCount);
        iCount++;
        Display();
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();


    return 0;
}
