/////////////////////////////////////////////////////////////////////////////
void Display()
{
    static int iCount = 5;

    if(iCount >= 1)
    {
        printf("%d\t",iCount);
        iCount--;
        Display();
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();


    return 0;
}
