/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display * given input times
//                  using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static int iCount = 1;

    if(iCount <= iNo)
    {
        printf("*\t");
        iCount++;
        Display(iNo);
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue  = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}
