#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display alphabets from A upto alphabet 
//                  corresponding to input number using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static int iCount = 1;

    static char ch = 'A';

    if(iCount <= iNo)
    {
        printf("%c\t",ch);
        ch++;
        iCount++;
        Display(iNo);
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}
