int SumDigits(int iNo)
{
    static int iSum = 0;
    static int iDigit = 0;
   
    if(iNo > 0)
    {
        iDigit = iNo % 10;
        iSum = iSum + iDigit;
        iNo = iNo / 10;
        SumDigits(iNo);
    }

    return iSum;

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue  = 0;
    int iRet = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    iRet = SumDigits(iValue);

    printf("Sum of digits is :%d",iRet);

    return 0;
}
