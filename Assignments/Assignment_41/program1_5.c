/////////////////////////////////////////////////////////////////////////////
//  Function name:  Product
//  Description:    It use to calculate product of  digits in given 
//                  number using recurrsion.
//  Input:          int
//  Output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////

int Product(int iNo)
{
    static int imult = 1;
    static int iDigit = 0;
   
    if(iNo > 0)
    {
        iDigit = iNo % 10;
        imult = imult * iDigit;
        iNo = iNo / 10;
        Product(iNo);
    }

    return imult;

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue  = 0;
    int iRet = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    iRet = Product(iValue);

    printf("Sum of digits is :%d",iRet);

    return 0;
}
