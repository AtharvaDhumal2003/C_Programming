#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//  Function name:  CheckLeapYear  
//  Description:    It is used to check is input year is Leap or not
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void CheckLeapYear(int year)
{
    if((year % 4)==0)
    {
        printf("It is Leap year");
    }
    else
    {
        printf("It is not a Leap year");
    }
}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int yr;
    printf("Enter year:");
    scanf("%d",&yr);
    CheckLeapYear(yr);
    return 0;
}
