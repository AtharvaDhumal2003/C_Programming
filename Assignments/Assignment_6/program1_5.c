/////////////////////////////////////////////////////////////////////////////
//  Function name:  CalculatePercentage  
//  Description:    It is used to calculate percentage
//  Input:          int,int
//  Output:         Float
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
