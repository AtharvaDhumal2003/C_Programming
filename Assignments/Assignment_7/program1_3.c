#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  DisplayNumberLine  
//  Description:    It is used to display nuber line of input number
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void DisplayNumberLine(int iNo)
{
    int iCnt = 0;

    for(iCnt=-iNo;iCnt<=iNo;iCnt++)
    {
        printf("%d\t",iCnt);
    }


}
//Time complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    printf("Enter Number:");
    scanf("%d",&iValue);

    DisplayNumberLine(iValue);
    return 0;

}
