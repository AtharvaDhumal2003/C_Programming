#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  OddDisplay  
//  Description:    It is used to display odd numbers till input number
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////

void OddDisplay(int iNo)
{
    int iCnt = 0;

    for(iCnt=1;iCnt<=iNo;iCnt+=2)
    {
        printf("%d\t",iCnt);
    }


}
//Time complexity:O(N/2)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    printf("Enter Number:");
    scanf("%d",&iValue);

    OddDisplay(iValue);
    return 0;

}
