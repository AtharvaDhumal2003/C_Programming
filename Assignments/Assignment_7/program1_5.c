#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////////
//  Function name:  MultipleDisplay 
//  Description:    It takes input number and displays its first five multiples
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
////////////////////////////////////////////////////////////////////////////////
void MultipleDisplay(int iNo)
{
    int iCnt = 0;
    int iNo2 = 1;
    for(iCnt=1;iCnt<=iNo,iNo2<=5;iCnt++,iNo2++)
    {
        printf("%d\t",iNo*iNo2);
    }


}
//Time complexity:O(N)



/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    printf("Enter Number:");
    scanf("%d",&iValue);

    MultipleDisplay(iValue);
    return 0;

}
