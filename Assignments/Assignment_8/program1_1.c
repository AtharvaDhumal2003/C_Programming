#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Number 
//  Description:    It is used to check given number is small or medium 
//                  or large
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void Number(int iNo)
{
    
    if(iNo<50)
    {
        printf("Small");
    }
    else if(iNo>50 && iNo<100)
    {
        printf("Medium");
    }
    else if(iNo>100)
    {
        printf("Large");
    }
}

//Time complexity:O(1)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue=0;
    printf("Enter Number:");
    scanf("%d",&iValue);

    Number(iValue);
    
    return 0;
}
