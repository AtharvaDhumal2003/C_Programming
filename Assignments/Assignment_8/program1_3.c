#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Factorial  
//  Description:    It is used to calculate factorial of given number
//  Input:          int
//  Output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
int Factorial(int iNo)
{
    int iCnt=0;
    int iFact=0;
    
    if(iNo<0)
    {
        iNo=-iNo;
    }
    for(iCnt=iNo,iFact=1;iCnt>0;iCnt--)
    {
        iFact=iFact*iCnt;
    }
    
    return iFact;

}
//Time complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0, iRet=0;

    printf("Enter Number:");
    scanf("%d",&iValue);

    iRet = Factorial(iValue);
    
    printf("Factorial of number is:%d",iRet);
    
    return 0;
}
