/////////////////////////////////////////////////////////////////////////////
//  Function name:  TableReverse  
//  Description:    It is used to display table in reverse order
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////

void TableReverse(int iNo)
{
    int iCnt =0;
    //int i = 0;
    if(iNo<0)
    {
        iNo=-iNo;
    }
    for(iCnt=10;iCnt>0;iCnt--)
    {
        printf("%d\t",iNo*iCnt);
    }
}
//Time Complexity:O(N)

/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    printf("Enter Number:");
    scanf("%d",&iValue);

    TableReverse(iValue);
    return 0;
}

