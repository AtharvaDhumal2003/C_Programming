#include<stdio.h>
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display  
//  Description:    It is used to display * and # given input times
//  Input:          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt=1;iCnt<=iNo;iCnt++)
    {
        printf("*\t");
    }
    
    for(iCnt=1;iCnt<=iNo;iCnt++)
    {
        printf("#\t");
    }

}
//Time Complexity:O(N)


/////////////////////////////////////////////////////////////////////////////
//  Entry point Function for given application
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0;
    printf("Enter Number:");
    scanf("%d",&iValue);

    Display(iValue);
    return 0;

}
