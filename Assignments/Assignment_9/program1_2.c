int DollarToInr(int iNo)
{
    iNo=iNo*70;

    return iNo;
}
///Time complexity:O(1)


/////////////////////////////////////////////////////////////////////////////
//  Entry point Function for given application
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue=0,iRet=0;
    printf("Enter Number of USD:");
    scanf("%d",&iValue);

    iRet =DollarToInr(iValue);

    printf("Value in INR is %d",iRet);
    return 0;

}
