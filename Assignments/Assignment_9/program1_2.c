/////////////////////////////////////////////////////////////////////////////
//  Function name:  DollarToInr
//  Description:    It is used to convert dollar to rupees
//  Input:          int
//  output          int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           27/10/2025
